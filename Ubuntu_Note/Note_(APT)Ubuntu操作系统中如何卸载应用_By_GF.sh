# Ubuntu命令行 apt purge 卸载应用

# 1.当在 Ubuntu 操作系统中卸载应用时，应用程序会被卸载，但是会留下细小的、修改过的用户配置文件。这些文件是故意被留下的，因为当你再次安装同样的应用时，会使用这些遗留的配置文件。

# 2.如果你想完全卸载应用，你可以使用  apt purge  命令代替  apt remove 命令，或者在 apt remove  命令使用之后再使用它。

# 3.命令行：

sudo apt purge program_name

# 注意：apt purge 命令不会删除保存在用户目录下的数据或者配置文件。

##################################################

# Ubuntu命令行 apt autoclean 清理系统

# 1.删除为了满足某些依赖安装的，但现在不再需要的软件包。

# 2.在终端输入如下命令：

sudo apt autoclean

# 这条命令很安全，而且会释放几百 MB 的磁盘空间。

